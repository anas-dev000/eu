/**
 * حساب النتيجة: عدد الإجابات التي تشير للخطر
 */
export const calculateScore = (answers) => {
  let score = 0;
  screeningQuestions.forEach((q) => {
    const userAnswer = answers[q.id];
    if (userAnswer === q.riskAnswer) {
      score += 1;
    }
  });
  return score;
};

/**
 * تحديد المستوى بناء على النتيجة
 */
export const getLevel = (score) => {
  if (score <= 2) return 1;
  if (score <= 6) return 2;
  return 3;
};

